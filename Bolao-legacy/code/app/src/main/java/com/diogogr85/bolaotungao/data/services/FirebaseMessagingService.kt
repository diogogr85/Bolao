package com.diogogr85.bolaotungao.data.services

import android.app.NotificationManager
import android.app.PendingIntent
import android.content.Context
import android.content.Intent
import android.media.RingtoneManager
import android.support.v4.app.NotificationCompat
import android.util.Log
import com.diogogr85.bolaotungao.R
import com.diogogr85.bolaotungao.ui.main.MainActivity
import com.google.firebase.messaging.RemoteMessage
import com.google.firebase.messaging.FirebaseMessagingService

open class BolaoFirebaseMessagingService: FirebaseMessagingService() {

    val TAG = "Service"
    val NOTIFICATION_INFO = 0xFC0001
    val NOTIFICATION_INFO_REQUEST = 0xFC0002

    override fun onMessageReceived(remoteMessage: RemoteMessage?) {
        // Handle FCM messages here.
        // If the application is in the foreground handle both data and notification messages here.
        // Also if you intend on generating your own notifications as a result of a received FCM
        // message, here is where that should be initiated.
        Log.d(TAG, "From: " + remoteMessage!!.from)
        Log.d(TAG, "Notification Message Body: " + remoteMessage.notification?.body!!)

        val message = remoteMessage.notification?.body
        message?.let {
            sendNotification(it)
        }
    }

    /**
     * Creates and shows a simple notification containing the received FCM message.
     * @param messageBody FCM message body received
     */
    private fun sendNotification(messageBody: String) {
        val intent = Intent(this, MainActivity::class.java)
        intent.addFlags(Intent.FLAG_ACTIVITY_CLEAR_TOP)
        val pendingIntent = PendingIntent.getActivity(this, NOTIFICATION_INFO_REQUEST, intent, PendingIntent.FLAG_ONE_SHOT)
        val defaultSoundUri = RingtoneManager.getDefaultUri(RingtoneManager.TYPE_NOTIFICATION)
        val notificationBuilder = NotificationCompat.Builder(this, "bolao")
                .setSmallIcon(R.drawable.ic_cow_black)
                .setContentTitle(messageBody)
//                .setContentText(messageBody)
                .setAutoCancel(true)
                .setSound(defaultSoundUri)
                .setContentIntent(pendingIntent)
        val notificationManager = getSystemService(Context.NOTIFICATION_SERVICE) as NotificationManager
        notificationManager.notify(NOTIFICATION_INFO, notificationBuilder.build())
    }

}